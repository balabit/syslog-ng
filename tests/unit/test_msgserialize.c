void
test_msg_serialize()
{
  LogMessage *msg = NULL;
  LogTemplate *template = NULL;
  GString *stream = g_string_new("");
  GString *output = g_string_new("");
  NVHandle test_handle = 0;
  SerializeArchive *sa = serialize_string_archive_new(stream);

  parse_options.flags |= LP_SYSLOG_PROTOCOL;
  log_msg_registry_init();
  test_handle = log_msg_get_value_handle("aaa");

  template = log_template_new(configuration, NULL);
  msg = log_msg_new(RAW_MSG, strlen(RAW_MSG), NULL, &parse_options);
  log_msg_set_value(msg, test_handle, "test_value", -1);
  log_template_compile(template, "${.SDATA.exampleSDID@0.eventSource}", NULL);
  log_template_append_format(template, msg, &configuration->template_options, LTZ_SEND, 0, NULL, output);
  assert_string(output->str, "Application", ERROR_MSG);

  log_msg_write(msg, sa);
  log_msg_unref(msg);
  log_template_unref(template);
  g_string_free(output, TRUE);
  log_msg_registry_deinit();

  log_msg_registry_init();

  output = g_string_new("");
  template = log_template_new(configuration, NULL);
  log_template_compile(template, "${.SDATA.exampleSDID@0.eventSource}", NULL);
  msg = log_msg_new_empty();

  assert_true(log_msg_read(msg, sa), ERROR_MSG);
  log_template_append_format(template, msg, &configuration->template_options, LTZ_SEND, 0, NULL, output);
  assert_string(output->str, "Application", ERROR_MSG);
  log_msg_unref(msg);
  serialize_archive_free(sa);
  log_template_unref(template);
  g_string_free(stream, TRUE);
  g_string_free(output, TRUE);
  log_msg_registry_deinit();
}

void
test_msg_serialize_old_versions(const guint8 *serialized_msg, gsize serialized_msg_len)
{
  LogMessage *msg = NULL;
  LogTemplate *template = log_template_new(configuration, NULL);;
  GString *stream= g_string_new("");
  GString *output = g_string_new("");
  SerializeArchive *sa;

  g_string_assign_len(stream, (gchar *) serialized_msg, serialized_msg_len);
  sa = serialize_string_archive_new(stream);

  log_msg_registry_init();

  msg = log_msg_new_empty();
  assert_true(log_msg_read(msg, sa), ERROR_MSG);
  log_template_compile(template, "${PROGRAM} ${MSG}", NULL);
  log_template_append_format(template, msg, &configuration->template_options, LTZ_SEND, 0, NULL, output);
  assert_string(output->str,
                "%PIX-6-302014 Teardown TCP connection 1688438 for bloomberg-net:1.2.3.4/8294 to inside:5.6.7.8/3639 duration 0:07:01 bytes 16975 TCP FINs",
                ERROR_MSG);

  log_msg_unref(msg);
  log_template_unref(template);
  serialize_archive_free(sa);
  g_string_free(stream, TRUE);
  g_string_free(output, TRUE);

  log_msg_registry_deinit();
}

int main(int argc, char **argv)
{
  log_msg_registry_init();
  init_and_load_syslogformat_module();
  log_template_global_init();
  test_msg_serialize();
  test_msg_serialize_old_versions(msg_serialized_version1, sizeof(msg_serialized_version1));
  test_msg_serialize_old_versions(msg_serialized_version10, sizeof(msg_serialized_version10));
  deinit_syslogformat_module();
  return 0;
}
